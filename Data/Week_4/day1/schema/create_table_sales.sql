CREATE TABLE sales(
	id INTEGER PRIMARY KEY,
	transaction_id INTEGER,
	bill_no INTEGER,
	bill_date TIMESTAMP,
	bill_location VARCHAR(255),
	customer_id INTEGER,
	product_id INTEGER,
	qty SMALLINT,
	uom VARCHAR(255),
	price FLOAT,
	gross_price FLOAT,
	tax_pc FLOAT,
	tax_amount FLOAT,
	discount_pc FLOAT,
	discount_amt FLOAT,
	net_bill_amt FLOAT,
	created_by VARCHAR(255),
	updated_by VARCHAR(255),
	created_date TIMESTAMP,
	updated_date TIMESTAMP
	)